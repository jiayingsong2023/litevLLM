def test_build_benchmark_requests_for_text_workload() -> None:
    requests = _build_benchmark_requests(
        prompt="Explain the benchmark.",
        request_count=2,
        workload="text",
    )

    assert len(requests) == 2
    assert requests[0].multi_modal_data is None
    assert "[benchmark_request_id=0]" in requests[0].prompt
    assert "[benchmark_request_id=1]" in requests[1].prompt


def test_build_benchmark_requests_for_multimodal_workload(tmp_path: Path) -> None:
    requests = _build_benchmark_requests(
        prompt="Describe the images.",
        request_count=2,
        workload="multimodal_multi",
        image_root=tmp_path,
        multimodal_images_per_request=3,
        multimodal_image_size=6,
    )

    assert len(requests) == 2
    assert requests[0].multi_modal_data is not None
    image_blocks = requests[0].multi_modal_data["image"]
    assert len(image_blocks) == 3
    for block in image_blocks:
        assert block["image"].startswith("file://")
        image_path = Path(block["image"][7:])
        assert image_path.is_file()


